#include <assert.h>
#include <stdio.h>
#include <string.h>

#include "quic_frame.h"
#include "quic_varint.h"

void test_varint_encode_roundtrip() {
    uint64_t values[] = { 0, 37, 63, 64, 15293, 494878333ULL };
    uint8_t buf[8];

    for (size_t i = 0; i < sizeof(values) / sizeof(values[0]); i++) {
        size_t offset = 0;
        uint64_t decoded = 0;
        int len = quic_encode_varint(values[i], buf, sizeof(buf));
        assert(len > 0);
        assert(quic_decode_varint(buf, (size_t)len, &offset, &decoded) == 0);
        assert(offset == (size_t)len);
        assert(decoded == values[i]);
    }

    printf("[PASS] VarInt encode/decode roundtrip\n");
}


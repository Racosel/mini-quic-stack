static void test_conn_recv_initial_applies_ack() {
    quic_connection_t conn;
    quic_cid_t odcid = { .len = 8, .data = {0x83, 0x94, 0xc8, 0xf0, 0x3e, 0x51, 0x57, 0x08} };
    uint8_t header[] = {
        0xd3, 0x6b, 0x33, 0x43, 0xcf, 0x08, 0x83, 0x94, 0xc8, 0xf0, 0x3e, 0x51, 0x57, 0x08,
        0x00, 0x00, 0x17, 0x00, 0x00, 0x00, 0x02
    };
    uint8_t plaintext[] = {
        0x02, 0x0a, 0x00, 0x01, 0x02, 0x01, 0x00,
        0x00, 0x00
    };
    uint8_t packet[128];
    size_t packet_len = 0;

    quic_conn_init(&conn);
    assert(quic_conn_set_initial_keys(&conn, QUIC_V2_VERSION, &odcid) == 0);

    quic_on_packet_sent(&conn.spaces[QUIC_PN_SPACE_INITIAL].in_flight, 5, 100, 1);
    quic_on_packet_sent(&conn.spaces[QUIC_PN_SPACE_INITIAL].in_flight, 8, 100, 1);
    quic_on_packet_sent(&conn.spaces[QUIC_PN_SPACE_INITIAL].in_flight, 9, 100, 1);
    quic_on_packet_sent(&conn.spaces[QUIC_PN_SPACE_INITIAL].in_flight, 10, 100, 1);

    assert(quic_packet_protect(&conn.spaces[QUIC_PN_SPACE_INITIAL].rx_crypto, 2, header, sizeof(header), sizeof(header) - 4,
                               plaintext, sizeof(plaintext), packet, sizeof(packet), &packet_len) == 0);
    assert(quic_conn_recv_initial(&conn, packet, packet_len) == 0);
    assert(conn.last_recv_space == QUIC_PN_SPACE_INITIAL);
    assert(conn.last_received_packet == 2);
    assert(conn.last_acked_packets == 4);
    assert(conn.spaces[QUIC_PN_SPACE_INITIAL].in_flight.bytes_in_flight == 0);
    assert(conn.spaces[QUIC_PN_SPACE_INITIAL].in_flight.largest_acked_packet == 10);

    printf("[PASS] Connection receive applies ACK frames\n");
}

